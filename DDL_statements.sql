	create table sale
(CUSTOM_ID int ,
PR_ID int,
EMP_ID int,
QTY int,
RATE INT,
AMOUNT INT,
BILL_DATE DATE,
VISIT_ID INT,
primary key (CUSTOM_ID,PR_ID,VISIT_ID)
);

create table customer
(CUSTOM_ID int ,
NAME varchar(20),
MOBILE_NUMBER int,
ADDRESS varchar(50),
primary key (CUSTOM_ID));

create table employee
(EMP_ID int ,
ADDRESS varchar(50),
NAME varchar(20),
SALARY INT,
primary key (EMP_ID);

create table working_hours
(EMP_ID int,
NO_OF_HOURS int,
WORK_DATE DATE,
primary key(EMP_ID,WORK_DATE));

create table product
(P_ID int ,
RATE int,
CATEGORY varchar(50),
NAME varchar(20),
primary key (P_ID) );

create table supplier
(
CONTACT INT,
NAME varchar(20),
S_ID  int,
ADDRESS varchar(50),
primary key (S_ID) );


create table supplier_prod_info
(
PROD_ID INT,
COST_PRICE INT,
S_ID  int,
primary key (S_ID,PROD_ID) );

create table restock
(NO_OF_SUPPLIES int,
PROD_ID INT,
COST_PRICE INT DEFAULT 0,
S_ID  int DEFAULT 0,
primary key (S_ID,PROD_ID) );


create table stock
(NUMBER_OF_STOCKS int,
PROD_ID int,
EXP_DATE DATE,
primary key (PROD_ID) );

